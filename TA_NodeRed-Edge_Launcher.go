package main

import (
	"os/exec" // 外部コマンドを実行するための標準パッケージ
	"syscall" // Windows固有のプロセス属性（ウィンドウ非表示）を設定するために使用
)

// -----------------------------------------------
// startHidden関数：指定したコマンドを「ウィンドウ非表示」で起動する
// -----------------------------------------------
func startHidden(name string, args ...string) {
	cmd := exec.Command(name, args...)                       // コマンドと引数を生成
	cmd.SysProcAttr = &syscall.SysProcAttr{HideWindow: true} // 起動時にウィンドウを表示しない設定
	_ = cmd.Start()                                          // バックグラウンドで実行
}

// -----------------------------------------------
// メイン関数
// -----------------------------------------------
func main() {
	//---------------------------------------------------
	// Microsoft Edge をアプリモードで起動する
	//
	// 接続先URL：Node-REDが動いているサーバーPC（192.168.0.115）のダッシュボード
	// ※ このPCにはNode-REDは入っていない、ブラウザで表示するだけ
	//
	// --app= オプションの効果：
	//   URLバーなし / タブなし / お気に入りバーなし
	//   独立アプリ風の表示になる
	//---------------------------------------------------
	startHidden(
		"cmd",
		"/C",
		"start",
		"",
		"msedge",
		"--app=http://192.168.0.115:1880/ui", // ← URLを変えたいときはここだけ書き換える
	)
}
